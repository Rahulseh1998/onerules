import type { StackProfile, ToolOutput } from "../types.js";
import { buildRuleSet, renderMarkdownRules, formatStackSummary } from "./common.js";

export function generateGemini(profile: StackProfile): ToolOutput {
  const rules = buildRuleSet(profile);
  const header = `# Project Rules\n\n> ${formatStackSummary(profile)} — generated by [onerules](https://github.com/onerules/onerules)`;
  const content = renderMarkdownRules(rules, header);

  return {
    toolId: "gemini",
    toolName: "Gemini CLI",
    filePath: "GEMINI.md",
    content,
  };
}
